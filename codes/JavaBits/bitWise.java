
class bitWise {
    public static void main(String[] args) {
        int a=5, b=6;
        
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        //not of a
        System.out.println(~a);
        //left shift a<<b then formula is a * 2 power b
        
        System.out.println(a<<b);
        //Right sift a>>b then formula is a / 2 power b
        System.out.println(a>>b);
        
    }
}

